// Функция для получения курса по ID
export const getCourseById = (id) => {
  return mockCourses.find((course) => course.id === parseInt(id));
};

// Функция для получения курсов по категории
export const getCoursesByCategory = (categoryId) => {
  return mockCourses.filter((course) => course.categoryId === categoryId);
};

// Функция для поиска курсов
export const searchCourses = (query) => {
  const lowerQuery = query.toLowerCase();
  return mockCourses.filter(
    (course) =>
      course.title.toLowerCase().includes(lowerQuery) ||
      course.author.toLowerCase().includes(lowerQuery) ||
      course.description.toLowerCase().includes(lowerQuery)
  );
};
